package com.paydock.core.domain.model

/**
 * Enumeration representing different environment types.
 * Defines the available environment options for an application.
 */
enum class Environment {
    /**
     * Represents the production environment.
     */
    PRODUCTION,

    /**
     * Represents the pre-production environment.
     */
    PRE_PRODUCTION,

    /**
     * Represents the staging environment.
     */
    STAGING
}